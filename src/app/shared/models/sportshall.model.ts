export class SportsHall {
  public name: string;
  public city: string;
  public street: string;
  public houseNumber: string;
  public phone: string;


  constructor(name: string, city: string, street: string, houseNumber: string, phone: string) {
    this.name = name;
    this.city = city;
    this.street = street;
    this.houseNumber = houseNumber;
    this.phone = phone;
  }
}
